void add_px(int16_t x, int16_t y,Color my_color,uint8_t opacity)
{     
  if(x < 0) return;
	if(y < 0) return;
	if(x > P3_W) return;
	if(y > P3_H) return;	
	if(opacity)
	{
			uint16_t locarion_r = 0*arrD1 + y*arrD2 + x;
			uint16_t locarion_g = 1*arrD1 + y*arrD2 + x;
			uint16_t locarion_b = 2*arrD1 + y*arrD2 + x;
		
			uint8_t oneminusalpha = 255 - opacity;
		
			Buffer_RGB[locarion_r] = (my_color.r * opacity)/255 + (oneminusalpha * Buffer_RGB[locarion_r])/255;
			Buffer_RGB[locarion_g] = (my_color.g * opacity)/255 + (oneminusalpha * Buffer_RGB[locarion_g])/255;
			Buffer_RGB[locarion_b] = (my_color.b * opacity)/255 + (oneminusalpha * Buffer_RGB[locarion_b])/255;
	}
}
void draw_rec(int16_t x,int16_t y,uint16_t w,uint16_t h,Color my_color,uint8_t opacity)
{
	for(int i=0;i<w;i++)
		 for(int k=0;k<h;k++)
			 add_px(x+i,y+k,my_color,opacity);
}
